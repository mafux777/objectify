#!/usr/bin/env node
/**
 * Reads the template JSON files and generates SQL INSERT statements
 * for seeding the `templates` table. Output goes to stdout.
 *
 * Usage: node scripts/generate-template-seed.mjs > supabase/seed.sql
 */
import { readFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const dataDir = join(__dirname, "..", "packages", "web", "src", "data");

const TEMPLATES = [
  { file: "objectify-workflow.json", name: "How Objectify Works", description: "Learn the basics of the Objectify diagram editor", featured: true, order: 0 },
  { file: "size-calibration.json", name: "Size Calibration Grid", description: "Test how different t-shirt sizes render across shapes", featured: false, order: 1 },
  { file: "text-capacity.json", name: "Text Capacity Grid", description: "How much Latin text fits in each shape and size combination", featured: false, order: 2 },
  { file: "example-a-microservices.json", name: "Web App Architecture", description: "Typical microservices web application architecture", featured: false, order: 3 },
  { file: "example-b-cicd-pipeline.json", name: "CI/CD Pipeline", description: "Continuous integration and deployment pipeline", featured: false, order: 4 },
  { file: "example-c-ecommerce-uml.json", name: "E-Commerce Components", description: "E-commerce system component diagram", featured: false, order: 5 },
  { file: "sample.json", name: "Project Thunderbattle", description: "Sample project architecture diagram", featured: false, order: 6 },
  { file: "trading-pipeline.json", name: "Trading Pipeline", description: "Financial trading data pipeline", featured: false, order: 7 },
  { file: "talos-components.json", name: "Talos Linux Components", description: "Talos Linux system components", featured: false, order: 8 },
  { file: "credits-on-objectify.json", name: "Credits on Objectify", description: "How credits work on Objectify: from first visit to wallet creation, credit usage, USDC reload, and optional account sign-up.", featured: false, order: 9 },
  { file: "import-export-workflows.json", name: "Import & Export Workflows", description: "Use cases for importing and exporting diagrams: share with teammates, feed to LLMs, version control, embed in docs, and more.", featured: false, order: 10 },
  { file: "how-to-get-usdc.json", name: "How to Get USDC", description: "Step-by-step guide to acquiring USDC on Coinbase and sending it to your Objectify address on the Base network.", featured: false, order: 11 },
];

function escapeSql(str) {
  return str.replace(/'/g, "''");
}

const values = TEMPLATES.map((t) => {
  const spec = readFileSync(join(dataDir, t.file), "utf-8");
  return `  (gen_random_uuid(), '${escapeSql(t.name)}', '${escapeSql(t.description)}', '${escapeSql(spec.trim())}'::jsonb, ${t.order}, ${t.featured})`;
});

console.log(`-- Auto-generated template seed data
-- Generated by: node scripts/generate-template-seed.mjs
-- Source: packages/web/src/data/*.json

INSERT INTO public.templates (id, name, description, spec, sort_order, featured)
VALUES
${values.join(",\n")}
ON CONFLICT DO NOTHING;
`);
